var mongoose = require("mongoose")
const verifySchema= mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    token: {
        type: String,
        required: true
    }
})
module.exports= mongoose.model('verify', verifySchema)